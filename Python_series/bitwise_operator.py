a= 10
b=8
print( "The binary of 10 is:",bin(a))
print("The binary of 8 is:",bin(b))
print("The binary of a&b is:",bin(a&b),"and The decimal value of a&b is:",a&b)
print("The binary of a|b is:",bin(a|b),"and The decimal value of a&b is:",a|b)
print("The binary of a^b is:",bin(a^b),"and The decimal value of a&b is:",a^b)



